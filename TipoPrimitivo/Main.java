public class Main {
    public static void main(String[] args) {
        // Tipo Inteiro
        byte menor = 19;
        System.out.println("Byte: " + menor);
        short medio = 1920;
        System.out.println("Short: " + medio);
        int medio2 = 192021;
        System.out.println("Int: " + medio);
        long grande = 19202122L;
        System.out.println("Long: " + grande);

        //Ponto Flutuante
        float flutu = 3.14F;
        System.out.println("Float: " + flutu);
        double ante = 3.14159;
        System.out.println("Double: " + ante);

        //Caracter
        char um = 'M';
        System.out.println("Char: " + um);

        //Boolean
        boolean idade = menor >= 18;
        System.out.println("Boolean: " + idade);
    }
}
