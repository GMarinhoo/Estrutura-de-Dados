public class Main {
    public static void main(String[] args) {
        // Tipo Inteiro
        byte menor = 19;
        System.out.println(menor);
        short medio = 1920;
        int medio2 = 192021;
        System.out.println(medio);
        long grande = 19202122L;
        System.out.println(grande);

        //Ponto Flutuante
        float flutu = 3.14F;
        System.out.println(flutu);
        double ante = 3.14159;
        System.out.println(ante);

        //Caracter
        char um = 'M';

        //Boolean
        boolean idade = menor >= 18;
        System.out.println(idade);
    }
}
